function secondBackground() {
    document.body.style.background = "url(background/bg2.jpg) no-repeat";
    document.getElementById('button').style.backgroundColor = "#3b4d42";
    document.getElementById('button').style.color = "#90a886"
    document.getElementById('text').style.color = "#90a886";
    document.getElementById('output').style.color = "#90a886";
    if (screenWidth >= 851) {
        document.body.style.backgroundSize = "100%";
    } else {
        document.body.style.backgroundSize = "400%";
    }
};

